func (c *GoGenerationContext) GenerateMethods() error {
	c.MethodsCode = codeBuilder.NewCodeBuilder()
	c.MethodsCode.WriteLine("// THIS FILE IS AUTOGENERATED BY A CCL TOOL. DO NOT EDIT.").
		NewLine().
		WriteLine("package " + c.Options.PackageName).
		NewLine().
		// default imports
		WriteLine("import (").
		Indent().
		WriteLine("\"bytes\"").
		WriteLine("\"encoding/binary\"").
		WriteLine("\"time\"").
		Unindent().
		WriteLine(")").
		NewLine().
		WriteLine("const (").
		Indent().
		WriteLine("_ = time.April").
		WriteLine("_ = bytes.MinRead").
		WriteLine("_ = binary.MaxVarintLen16").
		Unindent().
		WriteLine(")").
		NewLine()

	for _, currentTypeDef := range c.Options.CCLDefinition.TypeDefinitions {
		if currentTypeDef.IsCustomModel() {
			err := c.generateMethodsForModel(currentTypeDef.GetModelDefinition())
			if err != nil {
				return err
			}
		} else {
			// for now, we only support model type definitions
			return &cclErrors.UnsupportedTypeDefinitionError{
				TypeName:       currentTypeDef.GetFullName(),
				TargetLanguage: LanguageName,
			}
		}
	}

	return nil
}

func (c *GoGenerationContext) generateMethodsForModel(currentModel *CCLModel) error {
	c.MethodsCode.NewLine().
		WriteLine("//------------------------------------------------------------").
		NewLine().
		WriteLine("func (m *" + currentModel.Name + ") GetModelId() int {").
		Indent().
		WriteLine("return ModelId" + currentModel.Name).
		Unindent().
		WriteLine("}")

	// generate CloneEmpty() method
	if c.NeedsCloneMethods(gValues.LanguageGo, currentModel) {
		c.generateCloneMethods(currentModel)
	}

	if c.NeedsBinarySerialization(gValues.LanguageGo, currentModel) {
		c.generateSerializeBinaryMethod(currentModel)
		c.generateDeserializeBinaryMethod(currentModel)
	}
	return nil
}

