func (c *GoGenerationContext) GenerateCode() error {
	if c.Options.PackageName == "" {
		return errors.New("package name is required for Go code generation")
	}

	var err error
	err = c.GenerateConstants()
	if err != nil {
		return err
	}

	err = c.GenerateVars()
	if err != nil {
		return err
	}

	err = c.GenerateTypes()
	if err != nil {
		return err
	}

	err = c.GenerateHelpers()
	if err != nil {
		return err
	}

	err = c.GenerateMethods()
	if err != nil {
		return err
	}

	path := c.Options.OutputPath + string(os.PathSeparator)
	if c.ConstantsCode != nil {
		err = c.WriteCodeFile(path+ConstantsFileName, c.ConstantsCode.Build(nil))
		if err != nil {
			return err
		}
	}

	if c.VarsCode != nil {
		err = c.WriteCodeFile(path+VarsFileName, c.VarsCode.Build(nil))
		if err != nil {
			return err
		}
	}

	if c.TypesCode != nil {
		err = c.WriteCodeFile(path+TypesFileName, c.TypesCode.Build(nil))
		if err != nil {
			return err
		}
	}

	if c.HelpersCode != nil {
		err = c.WriteCodeFile(path+HelpersFileName, c.HelpersCode.Build(nil))
		if err != nil {
			return err
		}
	}

	if c.MethodsCode != nil {
		err = c.WriteCodeFile(path+MethodsFileName, c.MethodsCode.Build(nil))
		if err != nil {
			return err
		}
	}
	return nil
}

//---------------------------------------------------------

func (c *GoGenerationContext) GenerateConstants() error {
	c.ConstantsCode = codeBuilder.NewCodeBuilderWithOptions(&codeBuilder.CodeBuilderOptions{
		IndentationStr:  "\t",
		NewLineStr:      "\n",
		EnableDebugInfo: c.Options.GenerateDebugInfo,
	})
	//TODO: refactor this to use sections properly
	c.ConstantsCode.BeginSection("constants")
	defer c.ConstantsCode.EndSection()

	c.ConstantsCode.AddCommentHeader("// THIS FILE IS AUTOGENERATED BY A CCL TOOL. DO NOT EDIT.").
		AddHeader("package " + c.Options.PackageName).
		WriteLine("const (").
		Indent()

	for _, currentTypeDef := range c.Options.CCLDefinition.TypeDefinitions {
		if currentTypeDef.IsCustomModel() {
			err := c.generateConstantsForModel(currentTypeDef.GetModelDefinition())
			if err != nil {
				return err
			}
		} else {
			// for now, we only support model type definitions
			return &cclErrors.UnsupportedTypeDefinitionError{
				TypeName:       currentTypeDef.GetFullName(),
				TargetLanguage: CurrentLanguage.String(),
			}
		}
	}

	c.ConstantsCode.Unindent().
		WriteLine(")")
	return nil
}

func (c *GoGenerationContext) generateConstantsForModel(currentModel *CCLModel) error {
	c.ConstantsCode.WriteLine("ModelId" +
		currentModel.Name + " = " + ssg.ToBase10(currentModel.ModelId),
	)
	return nil
}

