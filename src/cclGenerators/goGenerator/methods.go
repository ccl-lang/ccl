func (c *GoGenerationContext) GenerateMethods() error {
	c.MethodsCode = codeBuilder.NewCodeBuilderWithOptions(&codeBuilder.CodeBuilderOptions{
		IndentationStr:  "\t",
		NewLineStr:      "\n",
		EnableDebugInfo: c.Options.GenerateDebugInfo,
	})
	//TODO: refactor this to use sections properly
	c.MethodsCode.BeginSection("methods")
	defer c.MethodsCode.EndSection()

	c.MethodsCode.AddCommentHeader("// THIS FILE IS AUTOGENERATED BY A CCL TOOL. DO NOT EDIT.").
		AddHeader("package " + c.Options.PackageName)

	c.registerGoMethodImports()

	for _, currentTypeDef := range c.Options.CCLDefinition.TypeDefinitions {
		if currentTypeDef.IsCustomModel() {
			err := c.generateMethodsForModel(currentTypeDef.GetModelDefinition())
			if err != nil {
				return err
			}
		} else {
			// for now, we only support model type definitions
			return &cclErrors.UnsupportedTypeDefinitionError{
				TypeName:       currentTypeDef.GetFullName(),
				TargetLanguage: CurrentLanguage.String(),
			}
		}
	}

	closeGoImportGroup(c.MethodsCode)
	return nil
}

func (c *GoGenerationContext) generateMethodsForModel(currentModel *CCLModel) error {
	modelName := currentModel.GetName()
	c.MethodsCode.MapVarPairs(
		"model", "*"+modelName,
		"modelName", modelName,
		"modelIdConst", "ModelId"+modelName,
	)
	defer c.MethodsCode.UnmapVar(
		"model",
		"modelName",
		"modelIdConst",
	)

	c.MethodsCode.WriteLine("//------------------------------------------------------------").
		NewLine().
		LineD("func (m $model) GetModelId() int {").
		Indent().
		LineD("return $modelIdConst").
		Unindent().
		WriteLine("}")

	// generate CloneEmpty() method
	if c.NeedsCloneMethods(CurrentLanguage, currentModel) {
		c.generateCloneMethods()
	}

	if c.NeedsBinarySerialization(CurrentLanguage, currentModel) {
		c.generateSerializeBinaryMethod(currentModel)
		c.generateDeserializeBinaryMethod(currentModel)
	}
	if c.NeedsJsonSerialization(CurrentLanguage, currentModel) {
		if err := c.generateSerializeJsonMethods(currentModel); err != nil {
			return err
		}
	}
	return nil
}

