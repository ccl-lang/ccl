func (c *GoGenerationContext) GenerateCode() error {
	if c.Options.PackageName == "" {
		return errors.New("package name is required for Go code generation")
	}

	var err error
	err = c.GenerateConstants()
	if err != nil {
		return err
	}

	err = c.GenerateVars()
	if err != nil {
		return err
	}

	err = c.GenerateTypes()
	if err != nil {
		return err
	}

	err = c.GenerateHelpers()
	if err != nil {
		return err
	}

	err = c.GenerateMethods()
	if err != nil {
		return err
	}

	path := c.Options.OutputPath + string(os.PathSeparator)
	if c.ConstantsCode != nil {
		err = ssg.WriteFileStr(path+ConstantsFileName, c.ConstantsCode.String())
		if err != nil {
			return err
		}
	}

	if c.VarsCode != nil {
		err = ssg.WriteFileStr(path+VarsFileName, c.VarsCode.String())
		if err != nil {
			return err
		}
	}

	if c.TypesCode != nil {
		err = ssg.WriteFileStr(path+TypesFileName, c.TypesCode.String())
		if err != nil {
			return err
		}
	}

	if c.HelpersCode != nil {
		err = ssg.WriteFileStr(path+HelpersFileName, c.HelpersCode.String())
		if err != nil {
			return err
		}
	}

	if c.MethodsCode != nil {
		err = ssg.WriteFileStr(path+MethodsFileName, c.MethodsCode.String())
		if err != nil {
			return err
		}
	}
	return nil
}

//---------------------------------------------------------

func (c *GoGenerationContext) GenerateConstants() error {
	c.ConstantsCode = codeBuilder.NewCodeBuilder()
	c.ConstantsCode.WriteLine("// THIS FILE IS AUTOGENERATED BY A CCL TOOL. DO NOT EDIT.").
		NewLine().
		WriteLine("package " + c.Options.PackageName).
		NewLine().
		WriteLine("const (").
		Indent()

	for _, currentTypeDef := range c.Options.CCLDefinition.TypeDefinitions {
		if currentTypeDef.IsCustomModel() {
			err := c.generateConstantsForModel(currentTypeDef.GetModelDefinition())
			if err != nil {
				return err
			}
		} else {
			// for now, we only support model type definitions
			return &cclErrors.UnsupportedTypeDefinitionError{
				TypeName:       currentTypeDef.GetFullName(),
				TargetLanguage: LanguageName,
			}
		}
	}

	c.ConstantsCode.Unindent().
		WriteLine(")")
	return nil
}

func (c *GoGenerationContext) generateConstantsForModel(currentModel *CCLModel) error {
	c.ConstantsCode.WriteLine("ModelId" +
		currentModel.Name + " = " + ssg.ToBase10(currentModel.ModelId),
	)
	return nil
}

