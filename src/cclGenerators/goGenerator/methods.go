func (c *GoGenerationContext) GenerateTypes() error {
	c.TypesCode = codeBuilder.NewCodeBuilderWithOptions(&codeBuilder.CodeBuilderOptions{
		IndentationStr:  "\t",
		NewLineStr:      "\n",
		EnableDebugInfo: c.Options.GenerateDebugInfo,
	})
	//TODO: refactor this to use sections properly
	c.TypesCode.BeginSection("types")
	defer c.TypesCode.EndSection()

	c.TypesCode.AddCommentHeader("// THIS FILE IS AUTOGENERATED BY A CCL TOOL. DO NOT EDIT.").
		AddHeader("package " + c.Options.PackageName)

	for _, currentTypeDef := range c.Options.CCLDefinition.TypeDefinitions {
		if currentTypeDef.IsCustomModel() {
			currentModel := currentTypeDef.GetModelDefinition()
			for _, currentField := range currentModel.Fields {
				targetType := currentField.Type
				if targetType.IsArray() {
					targetType = targetType.GetUnderlyingType()
				}
				if targetType.GetName() == cclValues.TypeNameDateTime {
					// Generated datetime fields use time.Time in the types file.
					registerGoImport(c.TypesCode, "time")
				}
			}
		}
	}
	closeGoImportGroup(c.TypesCode)

	// c.TypesCode.WriteString("type Serializable interface {\n")
	// c.TypesCode.WriteString("\tSerializeBinary() ([]byte, error)\n")
	// c.TypesCode.WriteString("\tDeserializeBinary(data []byte) error\n")
	// c.TypesCode.WriteString("\tCloneEmptySerializable() Serializable\n")
	// c.TypesCode.WriteString("}\n\n")

	for _, currentTypeDef := range c.Options.CCLDefinition.TypeDefinitions {
		if currentTypeDef.IsCustomModel() {
			err := c.generateTypesForModel(currentTypeDef.GetModelDefinition())
			if err != nil {
				return err
			}
		} else {
			// for now, we only support model type definitions
			return &cclErrors.UnsupportedTypeDefinitionError{
				TypeName:       currentTypeDef.GetFullName(),
				TargetLanguage: CurrentLanguage.String(),
			}
		}
	}

	return nil
}

func (c *GoGenerationContext) generateTypesForModel(currentModel *CCLModel) error {
	c.TypesCode.WriteLine("type " + currentModel.Name + " struct {").
		Indent()
	for _, currentField := range currentModel.Fields {
		targetType := currentField.Type

		// TODO: we have to check for other stuff in here
		// or maybe just refactor this part to a more clean code
		if targetType.IsArray() {
			targetType = targetType.GetUnderlyingType()
		}

		theGoType, ok := CCLTypesToGoTypes[targetType.GetName()]
		if !ok {
			if !targetType.IsCustomTypeModel() {
				return &cclErrors.UnsupportedFieldTypeError{
					TypeName:       targetType.GetName(),
					FieldName:      currentField.Name,
					ModelName:      currentModel.Name,
					TargetLanguage: CurrentLanguage.String(),
				}
			}

			customModel := targetType.GetDefinition().GetModelDefinition()
			if customModel == nil {
				return &cclErrors.UnsupportedFieldTypeError{
					TypeName:       targetType.GetName(),
					FieldName:      currentField.Name,
					ModelName:      currentModel.Name,
					TargetLanguage: CurrentLanguage.String(),
				}
			}

			// TODO: add ways to specify this type being pointer or not
			theGoType = "*" + customModel.Name
		}

		// TODO: handle extra operators here
		if currentField.IsArray() {
			theGoType = "[]" + theGoType
		}
		c.TypesCode.WriteLine(currentField.Name + " " + theGoType)
	}
	c.TypesCode.Unindent().
		WriteLine("}").
		NewLine()
	return nil
}

