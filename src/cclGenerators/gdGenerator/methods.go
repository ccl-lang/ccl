func (c *GDScriptGenerationContext) GenerateCode() error {
	c.ModelClasses = make(map[string]*codeBuilder.CodeBuilder)
	c.ModelSections = make(map[string][]string)
	c.OutputFiles = []string{}

	// Generate each model class
	for _, typeDef := range c.GetGenerationTypeDefinitions() {
		if typeDef.IsCustomModel() {
			fullName := typeDef.GetFullName()
			sections := c.ModelSections[fullName]
			if len(sections) == 0 {
				sections = codeBuilder.GetDefaultOrderedSections()
			}
			sections = ssg.AppendUnique(sections, fullName)
			c.ModelSections[fullName] = sections

			err := c.generateCodeForModel(typeDef.GetModelDefinition())
			if err != nil {
				return err
			}
		} else if typeDef.IsCustomEnum() {
			enumDef := typeDef.GetEnumDefinition()
			if enumDef.IsNested() {
				continue
			}

			err := c.generateCodeForEnum(enumDef)
			if err != nil {
				return err
			}
		} else {
			return &cclErrors.UnsupportedTypeDefinitionError{
				TypeName:       typeDef.GetFullName(),
				TargetLanguage: CurrentLanguage.String(),
			}
		}
	}

	return nil
}

func (c *GDScriptGenerationContext) generateCodeForEnum(enumDef *CCLEnum) error {
	builder := codeBuilder.NewCodeBuilderWithOptions(&codeBuilder.CodeBuilderOptions{
		IndentationStr:  "\t",
		NewLineStr:      "\n",
		EnableDebugInfo: c.Options.GenerateDebugInfo,
	})
	builder.BeginSection(enumDef.GetFullName())
	defer builder.EndSection()

	builder.AddCommentHeader("# THIS FILE IS AUTOGENERATED BY A CCL TOOL. DO NOT EDIT.")
	enumTypeName, err := c.getGDScriptEnumTypeName(enumDef)
	if err != nil {
		return err
	}

	builder.WriteLine("class_name " + enumTypeName).
		NewLine()
	if err := c.generateEnumDeclaration(builder, enumDef, true); err != nil {
		return err
	}

	path := c.Options.OutputPath + string(os.PathSeparator)
	fileName := DefaultFileNamingStyle.ApplyStyle(enumDef.Name)
	fullPath := path + fileName + ".gd"
	buildResult := builder.Build(nil)
	buildResult.Code = strings.TrimRight(buildResult.Code, " \t\r\n") + "\n"
	if err := c.WriteCodeFile(fullPath, buildResult); err != nil {
		return err
	}
	c.OutputFiles = append(c.OutputFiles, fullPath)

	return nil
}

func (c *GDScriptGenerationContext) generateCodeForModel(model *CCLModel) error {
	modelFullName := model.GetFullName()
	modelName := model.GetName()
	builder := codeBuilder.NewCodeBuilderWithOptions(&codeBuilder.CodeBuilderOptions{
		IndentationStr:  "\t",
		NewLineStr:      "\n",
		EnableDebugInfo: c.Options.GenerateDebugInfo,
	})
	c.ModelClasses[modelFullName] = builder

	builder.BeginSection(modelFullName)
	defer builder.EndSection()

	builder.MapVarPairs(
		"model", modelName,
	)
	defer builder.UnmapVar(
		"model",
	)

	err := c.generateModelClass(builder, model)
	if err != nil {
		return err
	}

	// Generate methods
	if c.NeedsBinarySerialization(gValues.LanguageGd, model) {
		if err = c.generateSerializeBinaryMethod(model, builder); err != nil {
			return err
		}

		if err = c.generateDeserializeBinaryMethod(model, builder); err != nil {
			return err
		}
	}
	if c.NeedsJsonSerialization(gValues.LanguageGd, model) {
		if err = c.generateSerializeJsonMethods(model, builder); err != nil {
			return err
		}
	}

	path := c.Options.OutputPath + string(os.PathSeparator)
	fileName, err := c.GetFileNameForModel(
		gValues.LanguageGd,
		model,
		DefaultFileNamingStyle,
		supportedFileNameStyles,
	)
	if err != nil {
		return err
	}

	fullPath := path + fileName + ".gd"
	buildResult := builder.Build(c.ModelSections[modelName])
	buildResult.Code = strings.TrimRight(buildResult.Code, " \t\r\n") + "\n"
	err = c.WriteCodeFile(fullPath, buildResult)
	if err != nil {
		return err
	}
	c.OutputFiles = append(c.OutputFiles, fullPath)

	// to not lack memory, we should delete the builder
	delete(c.ModelClasses, modelName)
	return nil
}

func (c *GDScriptGenerationContext) generateModelClass(builder *codeBuilder.CodeBuilder, model *CCLModel) error {
	modelName := model.GetFullName()
	needsSerializableInterface := c.needsSerializableInterface(model)

	// Write class header
	builder.AddCommentHeader("# THIS FILE IS AUTOGENERATED BY A CCL TOOL. DO NOT EDIT.")
	if err := c.generateModelAnnotations(builder, model); err != nil {
		return err
	}
	builder.WriteLine("class_name " + model.Name)

	if needsSerializableInterface {
		builder.WriteLine("implements BinarySerializable")
	}
	builder.NewLine()

	// Write model ID constant
	modelIdConstName := "MODEL_ID_" + strings.ToUpper(caseUtils.ToSnakeCase(model.Name))
	builder.WriteLine("const " + modelIdConstName +
		" = " + ssg.ToBase10(model.ModelId)).
		NewLine()

	for _, enumDef := range model.Enums {
		if err := c.generateEnumDeclaration(builder, enumDef, true); err != nil {
			return err
		}
		builder.NewLine()
	}

	// Write class variables
	for _, field := range model.Fields {
		varType, err := c.getGDScriptType(field)
		if err != nil {
			return err
		}
		if varType == "" {
			return &cclErrors.UnsupportedFieldTypeError{
				TypeName:       field.Type.GetName(),
				FieldName:      field.Name,
				ModelName:      modelName,
				TargetLanguage: CurrentLanguage.String(),
			}
		}
		fieldLine := "var " + caseUtils.ToSnakeCase(field.Name) + ": " + varType
		if field.HasDefaultValue() {
			defaultValue := gdDefaultLiteral(field.GetDefaultValue())
			if enumRef := c.GetEnumDefaultReference(field); enumRef != nil {
				ref, err := c.getGDScriptEnumReference(enumRef.Enum, enumRef.Member)
				if err != nil {
					return err
				}
				defaultValue = ref
			}
			fieldLine += " = " + defaultValue
		}
		builder.WriteLine(fieldLine)
	}
	builder.NewLine()

	// Add get_model_id method
	builder.WriteLine("func get_model_id() -> int:").
		Indent().
		WriteLine("return " + modelIdConstName).
		UnindentLine()

	// Add clone_empty method
	builder.WriteLine("func clone_empty() -> " + model.Name + ":").
		Indent().
		WriteLine("return " + model.Name + ".new()").
		UnindentLine()

	return nil
}

func (c *GDScriptGenerationContext) generateEnumDeclaration(
	builder *codeBuilder.CodeBuilder,
	enumDef *CCLEnum,
	named bool,
) error {
	header := "enum {"
	if named {
		enumDeclarationName, err := c.getGDScriptEnumDeclarationName(enumDef)
		if err != nil {
			return err
		}
		header = "enum " + enumDeclarationName + " {"
	}

	builder.WriteLine(header).
		Indent()
	for _, member := range enumDef.Members {
		memberName, err := c.getGDScriptEnumMemberName(enumDef, member)
		if err != nil {
			return err
		}
		builder.WriteLine(memberName + " = " + ssg.ToBase10(member.Value) + ",")
	}
	builder.Unindent().
		WriteLine("}")

	return nil
}

func (c *GDScriptGenerationContext) generateModelAnnotations(
	builder *codeBuilder.CodeBuilder,
	model *CCLModel,
) error {
	attrs := c.GetGlobalOrModelAttributes(
		CurrentLanguage,
		cclAttr.AttrAddAnnotation,
		model,
	).Attrs
	for _, attr := range attrs {
		param := attr.GetParamAt(0)
		if param == nil {
			return &cclErrors.InvalidAttributeUsageError{
				AttrName:       attr.Name,
				Message:        "requires a first parameter containing the raw annotation text",
				SourcePosition: attr.SourcePosition,
			}
		}

		builder.AppendLine(param.GetAsString())
	}

	return nil
}

func (c *GDScriptGenerationContext) getGDScriptType(field *cclValues.ModelFieldDefinition) (string, error) {
	targetType := field.Type
	if targetType.IsArray() {
		targetType = targetType.GetUnderlyingType()
	}

	gdType := CCLTypesToGdTypes[targetType.GetName()]
	if gdType == "" {
		if targetType.IsCustomTypeEnum() {
			enumTypeReference, err := c.getGDScriptEnumTypeReference(targetType)
			if err != nil {
				return "", err
			}
			gdType = enumTypeReference
		} else if !targetType.IsCustomTypeModel() {
			return "", nil
		} else {
			gdType = targetType.GetName()
		}
	}

	if field.IsArray() {
		gdType = "Array[" + gdType + "]"
	}

	return gdType, nil
}

func (c *GDScriptGenerationContext) needsSerializableInterface(model *CCLModel) bool {
	params := c.GetGlobalOrModelAttributes(
		CurrentLanguage,
		"ImplementSerializableInterface",
		model,
	).GetParamsAt(0)
	if len(params) == 0 {
		return false
	}

	return params[0].GetAsBool()
}
